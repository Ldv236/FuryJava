package ru.ldv236.core.Condition;

import java.util.Scanner;

public class Kafe {
    public static void main(String[] args) {
        // ПОСЛЕ ПОКАЗА свитч-кейс (показать на днях недели и на разделении будний/выходной
        // также показать оба варианта свитч-кейс - с присвоением и без

        /*
            У клиента есть выбор из трёх категорий блюд: завтрак, обед и ужин.
        Клиент указывает, голоден ли он сейчас или просто хочет что-то лёгкое.
            Если клиент голоден и выбрал:
        Завтрак: предложить омлет с тостами.
        Обед: предложить стейк с картофелем.
        Ужин: предложить пасту с соусом.

            Если клиент не голоден и выбрал:
        Завтрак: предложить йогурт с фруктами.
        Обед: предложить салат с курицей.
        Ужин: предложить суп-пюре.

            Дополнительно, программа должна предложить напиток:
        Если клиент выбрал завтрак, предложить кофе.
        Если обед или ужин — предложить сок.
        */

        Scanner scanner = new Scanner(System.in);

        // Ввод данных
        System.out.println("Введите категорию (завтрак, обед, ужин): ");
        String category = scanner.nextLine().toLowerCase();

        System.out.println("Вы голодны? (да/нет): ");
        boolean isHungry = scanner.nextLine().equalsIgnoreCase("да");

        String dish;
        String drink = category.equals("завтрак") ? "кофе" : "сок";

        // Проверка голода
        if (isHungry) {
            // Если голоден
            dish = switch (category) {
                case "завтрак" -> "омлет с тостами";
                case "обед" -> "стейк с картофелем";
                case "ужин" -> "паста с соусом";
                default -> null; // Некорректная категория
            };
        } else {
            // Если не голоден
            dish = switch (category) {
                case "завтрак" -> "йогурт с фруктами";
                case "обед" -> "салат с курицей";
                case "ужин" -> "суп-пюре";
                default -> null; // Некорректная категория
            };
        }

        // Проверка корректности категории
        if (dish == null) {
            System.out.println("Ошибка: некорректная категория.");
        } else {
            // Вывод результата
            System.out.println("Рекомендуем " + dish + " и " + drink + ".");
        }
    }
}
